            <h1 class="section-title">Actionable Insights</h1>
            <p>After the analysis is completed, the SWOT provides clear results that can be applied directly and immediately to modify water treatment parameters and improve safety of water at the household level. In addition to the target recommendation, the analysis provides a summary of the current water safety at the site, and a predicted improvement that would be possible by implementing the SWOT recommendation.</p>
            <p>This information is critical for deciding what action is needed to improve water safety at the sites you manage. This could mean increasing free chlorine residual at the tapstand, rolling out a jerrycan cleaning campaign or slowing chlorine decay through better water storage. Based on your site-specific needs we can help you identify the most appropriate measures to improve water safety.</p>
