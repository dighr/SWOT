            <h1 class="section-title">Routine Monitoring</h1>
            <p>The SWOT works with the water quality monitoring data you already collect, and you can get started with the equipment and staff you already use. To get a reliable recommendation you need to provide around 150 paired samples, each with a measure of free residual chlorine taken at the tapstand and again at the household. This data can be collected for a site as a specific exercise carried out every few months, or as an ongoing process.</p>
            <p>We have monitoring tools, templates, and detailed instructions available as well as a support desk if you need more in-depth advice. We have experience supporting teams working with online and bulk chlorination of piped water networks, as well as in contexts with water trucking and handpump chlorinators. Whatever your use case, we are here to support you to get the most out of the SWOT.</p>
