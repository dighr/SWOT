            <h1 class="section-title">Advanced Analytics</h1>
            <p>The SWOT applies novel machine-learning and numerical modelling tools to the problem of post-distribution chlorine decay modelling. These modelling tools come from years of water safety research in refugee camps in South Sudan, Jordan, Rwanda, Tanzania, Bangladesh (and elsewhere) by our team at York University, the University of California, Berkeley, and field partners at MSF and UNHCR.</p>
            <p>Because the models are based on real, site specific, monitoring data they account for all the factors that affect the rate of chlorine decay, from water temperature to handling practices and the type of storage containers being used.</p>
