            <h1 class="section-title">The Problem</h1>
            <p>Safe water is essential for protecting public health during humanitarian emergencies. Waterborne diseases like cholera and Hepatitis E are some of the leading causes of preventable illness in refugee and displacement camps around the world.</p>
            <p>Treating water with chlorine is a safe and effective way to destroy harmful pathogens and protect it from recontamination. But in humanitarian settings knowing how much chlorine is needed for a specific site is challenging. In many places, applying the common guidance simply isn't sufficient to ensure water is protected for long enough to prevent waterborne diseases.</p>
            <p>The answer isn't just to add more chlorine because the taste and smell can be off-putting for some people. What's needed is an easy way to provide reliable, site-specific guidance for chlorine treatment that takes account of these issues.</p>
